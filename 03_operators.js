let a = 4
let b = 5

//Arithmetic
console.log("a + b: ", a+b)
console.log("a - b: ", a-b)
console.log("a * b: ", a*b)
console.log("a / b: ", a/b)
console.log("a % b: ", a%b)
console.log("a ** b: ", a**b)
console.log("a++: ", a++)       //value used first then incremented
console.log("b--: ", b--)       //value used first then decremented
console.log("++a: ", ++a)       //value first incremented then used
console.log("--b: ", --b)       //value first decremented then used

